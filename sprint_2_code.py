# =========================
# 1️⃣ Load Libraries
# =========================
import numpy as np
import pandas as pd
from scipy.io import loadmat
from zipfile import ZipFile
from sklearn.model_selection import GroupKFold
from sklearn.preprocessing import StandardScaler, MinMaxScaler
from sklearn.metrics import mean_squared_error, r2_score
from sklearn.ensemble import RandomForestRegressor
import xgboost as xgb

# =========================
# 2️⃣ Oxford Loader
# =========================
def oxford_to_dataframe(mat):
    records = []

    for key in mat.keys():
        if "Cell" not in key:
            continue
        cell = mat[key][0,0]

        for field_name in cell.dtype.names:
            cycle_data = cell[field_name][0,0]

            # Extract voltage, temp, capacity from correct tuple indices
            def extract_mean(arr_field, idx):
                try:
                    arr = arr_field[0,0][idx]
                    return np.mean(arr)
                except:
                    return np.nan

            voltage = extract_mean(cycle_data['C1ch'], 1)
            temperature = extract_mean(cycle_data['C1ch'], 3)
            capacity = extract_mean(cycle_data['OCVch'], 1)

            if np.isnan(voltage) or np.isnan(temperature) or np.isnan(capacity):
                continue

            records.append({
                "battery_id": key,
                "cycle": int(field_name.replace('cyc','')),
                "voltage": voltage,
                "temperature": temperature,
                "capacity": capacity
            })
    return pd.DataFrame(records).reset_index(drop=True)

# =========================
# 3️⃣ Load NASA Dataset
# =========================
nasa_zip_path = '/content/drive/MyDrive/NASA Battery Degradation Dataset (Cycle-Level CSV).zip'
nasa_frames = []

with ZipFile(nasa_zip_path,'r') as zip_ref:
    csv_files = [f for f in zip_ref.namelist() if f.endswith(".csv")]
    for file in csv_files:
        with zip_ref.open(file) as f:
            df = pd.read_csv(f)
            df['battery_id'] = file.replace('.csv','')
            nasa_frames.append(df)
nasa_data = pd.concat(nasa_frames, ignore_index=True)

# =========================
# 4️⃣ Load Oxford Dataset
# =========================
oxford_mat_path = '/content/drive/MyDrive/Oxford_Battery_Degradation_Dataset_1.mat'
mat = loadmat(oxford_mat_path)
oxford_data = oxford_to_dataframe(mat)

# =========================
# 5️⃣ Compute SOH and RUL
# =========================
def compute_targets(df):
    df = df.sort_values(['battery_id','cycle'])
    df['initial_capacity'] = df.groupby('battery_id')['capacity'].transform('first')
    df['soh'] = df['capacity'] / df['initial_capacity']
    max_cycle = df.groupby('battery_id')['cycle'].transform('max')
    df['rul'] = max_cycle - df['cycle']
    return df

nasa_data = compute_targets(nasa_data)
oxford_data = compute_targets(oxford_data)

# =========================
# 6️⃣ Combine Datasets
# =========================
data = pd.concat([nasa_data, oxford_data], ignore_index=True)

# =========================
# 7️⃣ Feature Engineering
# =========================
base_features = ['voltage','temperature','capacity']
LAGS = 5

for f in base_features:
    for lag in range(1,LAGS+1):
        data[f"{f}_lag{lag}"] = data.groupby('battery_id')[f].shift(lag)
    data[f"{f}_diff"] = data.groupby('battery_id')[f].diff()

data['capacity_fade'] = data.groupby('battery_id')['capacity'].pct_change()
data['cycle_norm'] = data['cycle'] / data.groupby('battery_id')['cycle'].transform('max')
data = data.dropna().reset_index(drop=True)

# =========================
# 8️⃣ Prepare Training Data
# =========================
features = [c for c in data.columns if c not in ['battery_id','cycle','soh','rul','initial_capacity']]
X = data[features]
y_soh = data['soh']
y_rul = data['rul']

# Normalize RUL for better training
rul_scaler = MinMaxScaler()
y_rul_scaled = rul_scaler.fit_transform(y_rul.values.reshape(-1,1)).flatten()

groups = data['battery_id']

# =========================
# 9️⃣ Models
# =========================
models = {
    "Random Forest": RandomForestRegressor(n_estimators=300, max_depth=10, random_state=42),
    "XGBoost": xgb.XGBRegressor(n_estimators=500, max_depth=6, learning_rate=0.05, random_state=42)
}

# =========================
# 🔟 Cross-Validation
# =========================
gkf = GroupKFold(n_splits=5)
results = {name: {'soh_rmse':[],'soh_r2':[],'rul_rmse':[],'rul_r2':[]} for name in models}

for fold,(train_idx,test_idx) in enumerate(gkf.split(X,y_soh,groups)):
    print("Fold",fold+1)
    X_train, X_test = X.iloc[train_idx], X.iloc[test_idx]
    y_train_soh, y_test_soh = y_soh.iloc[train_idx], y_soh.iloc[test_idx]
    y_train_rul, y_test_rul = y_rul_scaled[train_idx], y_rul_scaled[test_idx]

    scaler = StandardScaler()
    X_train = scaler.fit_transform(X_train)
    X_test = scaler.transform(X_test)

    for name, model in models.items():
        import copy
        m_soh = copy.deepcopy(model)
        m_rul = copy.deepcopy(model)

        m_soh.fit(X_train, y_train_soh)
        m_rul.fit(X_train, y_train_rul)

        pred_soh = m_soh.predict(X_test)
        pred_rul_scaled = m_rul.predict(X_test)
        pred_rul_orig = rul_scaler.inverse_transform(pred_rul_scaled.reshape(-1,1)).flatten()
        y_test_rul_orig = rul_scaler.inverse_transform(y_test_rul.reshape(-1,1)).flatten()




        results[name]['soh_rmse'].append(np.sqrt(mean_squared_error(y_test_soh,pred_soh)))
        results[name]['soh_r2'].append(r2_score(y_test_soh,pred_soh))
        results[name]['rul_rmse'].append(np.sqrt(mean_squared_error(y_test_rul_orig,pred_rul_orig)))
        results[name]['rul_r2'].append(r2_score(y_test_rul_orig,pred_rul_orig)
)
# =========================
# 1️⃣1️⃣ Final Results
# =========================
print("\nAverage Results:\n")
for name in results:
    print(name)
    print("SOH RMSE:", np.mean(results[name]['soh_rmse']))
    print("SOH R2:", np.mean(results[name]['soh_r2']))
    print("RUL RMSE:", np.mean(results[name]['rul_rmse']))
    print("RUL R2:", np.mean(results[name]['rul_r2']))
    print()